package Ch2;

public class EvenOdd {

	public static void main(String[] args) {
		int number;
		
		number = 100;
		
		if(number % 10 == 0)
		{
			System.out.println("Number is Even Number");
		}
		else 
		{
			System.out.println("Number is Odd number");
		}
			System.out.println("BYE! :-)");
	}
			
}
