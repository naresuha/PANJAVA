package Ch2;

public class LeapYear {

	public static void main(String[] args) {
		int Year;
		Year = 2016;
		if (Year % 4 == 0)
		{
			System.out.println("Year is a Leap Year");
		}
		else
			System.out.println("Year is Non Leap Year");
	}

}
